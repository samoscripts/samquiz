<?php

namespace App\Infrastructure\Persistence\Trait;

use Doctrine\ORM\Mapping as ORM;

trait IdEntity
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column]
    private ?int $id = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function setId(int $id): self
    {
        $this->id = $id;
        return $this;
    }
}

